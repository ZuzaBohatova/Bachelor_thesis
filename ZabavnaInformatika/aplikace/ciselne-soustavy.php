<h1>Číselné soustavy</h1>
<p>Každý z vás zná čísla v běžné desítkové soustavě, např. 3, 42, 904 ... 
Ve světě počítačů se všechna data převádí na čísla v dvojkové soustavě, tedy kombinace 0 a 1.
Ale proč to tak je? Abychom data mohly přenášet a ukládat, musí jim rozumět přenosová media - kabely a různé elektrické obvody. 
Kdybychom měli převádět čísla 0-9 do elektrické podoby, naše systémy by byly velmi složité, pomalé a hlavně poruchové. 
Proto používáme jen 0 a 1, elektrickým obvodům pak stačí rozeznávat jen dva stavy, např. vypnuto/zapnuto.</p>

<p>
Jak převádět čísla do desítkové soustavy? 
Princip je jednoduchý. Číslo dělíme 2, dokud je to možné. Zapisujeme si zbytek po dělení, který je vždy 1 nebo 0. 
Tato řada 1 a 0 s posledním zbytkem na začátku je naše původní číslo ve dvojkové soustavě. 
</p>

